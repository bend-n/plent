pub fn replace(s: &str) -> String {
    let mut o = String::with_capacity(s.len());
    for ch in s.chars() {
        if let Some(mtch) = E2E.get(&ch) {
            o.push_str(mtch)
        } else {
            o.push(ch);
        }
    }
    o
}

